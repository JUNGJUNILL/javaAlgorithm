package javaBasic;

public class P004_while {

	public static void main(String[] args) {

		//https://java119.tistory.com/105
		int num=3; 
		int n=3; 
		while(num-- > 0){
			System.out.println(num);
		}
		System.out.println("----------------------");
		
		for(int i=0; i<n; i++){
			System.out.println(i);
		}
		
	}

}
